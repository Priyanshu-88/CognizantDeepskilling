public class Operators {
    public static void main(String[] args) {
        //arithmetic operator
        int a = 2;

        //assignment operator
        int b = 4 + a;
        b += 5;

        //Modulo Operator
        int c = 6 % 4;
        System.out.println(b);

        //Comparison Operator
        System.out.println(69 == 68);

        //logical Operator
        System.out.println(68 > 34 && 65 < 22);
    }
}
