public class SwitchStatement {
    public static void main(String[] args){
        int age = 24;

        switch(age){
            case 38:
                System.out.println("Senior Citizens");
                break;
            case 24:
                System.out.println("Adult");
                break;
            case 18:
                System.out.println("Child");
                break;
        }
    }
}
